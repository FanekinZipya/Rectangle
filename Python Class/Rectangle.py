class Rectangle:
    lenght = 0
    width = 0
    area = 0

    def __init__(self, l, w):
        self.lenght = l
        self.width = w

    def Area(self):
        print('Lenght = ' + str(self.lenght))
        print('Width = ' + str(self.width))
        print('Area of Rectangle = ' + str(self.area))

    def Calculation(self):
        self.area = self.lenght*self.width
